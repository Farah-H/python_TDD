# This file contains our simple calculator class which we will test
class Simple_Calc:
    
    # addition method
    def add(self,a,b):
        return a + b
    
    # subtraction method
    def subtract(self,a,b):
        return a - b
    
    # multiplication method
    def multiply(self,a,b):
        return a * b
    
    # division method
    def devide(self,a,b):
        return a / b